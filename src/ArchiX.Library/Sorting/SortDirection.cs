namespace ArchiX.Library.Sorting;

/// <summary>
/// Sıralama yönünü temsil eder.
/// </summary>
public enum SortDirection
{
    /// <summary>
    /// Artan sıralama (A → Z, küçükten büyüğe).
    /// </summary>
    Ascending = 0,

    /// <summary>
    /// Azalan sıralama (Z → A, büyükten küçüğe).
    /// </summary>
    Descending = 1
}
